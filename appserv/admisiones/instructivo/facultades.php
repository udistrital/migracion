           <p align="justify"> En correspondencia con la disponibilidad de cupos aprobados por  el Consejo Académico, la selección de los aspirantes a los programas de  ingeniería de la   Facultad Tecnológica se hará de mayor a menor puntaje con  base en la sumatoria de: puntaje del examen de estado (que concede como máximo  300 puntos), promedio académico acumulado en la carrera tecnológica (que  concede como máximo 300 puntos), puntaje asignado por la afinidad del plan de  estudios culminado con la carrera a la que aspira (que concede como máximo 300  puntos) y puntaje asignado por experiencia laboral (que concede como máximo 100  puntos teniendo en cuenta la experiencia laboral certificada, posterior a la  obtención del título de tecnólogo). El máximo puntaje posible es de 1.000  puntos.</p>
           <p align="justify"> La legalización  de las inscripciones a los Programas Académicos de Ingeniería, se realiza  entregando los siguientes documentos foliados, <strong>el siguiente d&iacute;a   hábil posterior a la realización de la inscripción por la página Web</strong>,  en la Carrera  8 No 40-62 Edificio Sabio Caldas, Primer Piso:</p>
