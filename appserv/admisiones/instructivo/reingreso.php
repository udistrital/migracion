              <li class="Estilo16">Se&ntilde;or Estudiante tenga en cuenta que se debe acercar con la hoja de inscripci&oacute;n impresa de internet, y el reporte del examen de estado (Icfes) a la Carrera 8 No. 40 - 62 edificio Sabio Caldas primer piso Oficina de Admisiones el siguiente d&iacute;a h&aacute;bil despues de haber realizado la inscripci&oacute;n en la p&aacute;gina Web, con el fin de validar esta. </li>
