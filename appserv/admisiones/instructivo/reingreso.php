          <p align="justify">Aceptación de las transferencias: El Decano y el Coordinador de la carrera estudian la solicitud y determinan la situación académica en que se recibe el estudiante transferido. (Acuerdo No 001 del Consejo Académico, de junio 16 de 2000). </p>
          
		  <p align="justify">Las transferencias internas para las Facultades de Ingeniería, Ciencias y Educación, y Medio Ambiente y Recursos Naturales, deben cumplir los siguientes requisitos:

		  <ul>
            <li>Haber cursado y aprobado como mínimo dos (2) semestres del Proyecto Curricular del cual se quiere transferir, y mínimo debe cursar el cincuenta por ciento (50%) de la totalidad del plan de estudios al que ingresa.</li>
            <li>Tener como mínimo tres punto cinco (3.5) de promedio acumulado en esos dos semestres o los que haya cursado.</li>
            <li>No haber perdido ninguna asignatura.</li>
            <li>Tener un puntaje Icfes ponderado de acuerdo con lo aprobado en el Consejo Académico, igual al último admitido al programa donde quiere ingresar en el período respectivo.</li>
