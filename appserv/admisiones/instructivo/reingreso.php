            Las transferencias internas de los tecnólogos de la Universidad a la Facultad de Ingeniería, se aceptarán de acuerdo a los criterios académicos establecidos por el Consejo de Facultad y aprobados por el Consejo Académico (Acuerdo No. 03 del 29 de mayo de 2001).
